namespace {
    const std::string THEME_FILE = "/home/mateusdall/.cache/current_theme.txt";
}

std::string Theme::getCurrentThemeName() {
    std::ifstream file(THEME_FILE);
    if (!file.is_open()) {
        return "";
    }
    std::string themeName;
    std::getline(file, themeName);
    return themeName;
}

